package gmae.adventure;


public enum ActionType {
    MOVE,       // Move to an adjacent realm/cell
    DEFEND,     // Take a defensive stance
    USE_ITEM,   // Use an item from inventory
    TRADE,      // Initiate a trade sequence
    BUY,        // Buy goods at a market
    SELL,       // Sell goods at a market
    PASS        // Skip
}
